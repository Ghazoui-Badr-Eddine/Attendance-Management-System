      <h2>Mot Du <strong>Directeur</strong></h2>
      <hr>
    <div class="clearfix" >
      <p  align="justify"></br></br></br>
         L’Ecole Nationale Superieur d'Arts et Métiers de Casablanca–ENSAM– a été créée et implantée à l’Université HASSAN II –UHII- de la ville de Casablanca, cœur d’un important bassin industriel en pleine extension pour répondre aux besoins croissants en formations d’ingénieurs dans notre pays.
<br>
<br>Bénéficiant d’un tel environnement, l’ENSAM a pour missions d’œuvrer à dispenser une offre de formation évolutive en adéquation permanente avec les métiers et professions générés par les mutations économiques nationale et internationale, insuffler un dynamisme pour la recherche scientifique.</br>
<br>Pour accomplir cette mission, l’ENSAM bâtit son action sur trois leviers essentiels :</br></br>
 *le dynamisme incontestable de ses équipes pédagogique et administrative.</br>
*les compétences locales, les moyens disponibles et les actions de partenariats développées avec les opérateurs socioéconomiques.</br>
*l’engagement et l’esprit d’appartenance des élèves ingénieurs.</br>
*Offrir un lieu d’épanouissement pour mener des projets de Recherche et de recherche industrielle.</br>
*Créer un cadre de coopération fertile sur le plan régional, national et international.</br>
<br>
Se basant sur ces leviers l’ENSAM engage sa politique de gouvernance en visant à assurer un développement pérenne de l’école, à assoir et consolider son ancrage dans le tissu socio-économique et culturel, à promouvoir davantage la formation et la recherche scientifique au rang de l’excellence et à s’ouvrir davantage à l’international par des projets d’accompagnement à vocation de formation et recherche.
<br></br>
Au terme de leur cursus, les élèves ingénieurs de l’ENSAM se retrouvent alors dotés de compétences scientifiques de qualité, d’un esprit citoyen, d’une large culture générale et d’une bonne connaissance de l’entreprise. </br>
</p>
